#!/usr/bin/env bash

set -euo pipefail

delete="false"
while getopts ":dh" option; do
  case "$option" in
    d)
      delete="true"
      ;;
    h)
      echo "Usage: command [-hf] [-p <file_path>]"
      exit 0
      ;;
    :)
      echo "Option -OPTARG requires an argument" >&2
      exit 1
      ;;
    \?)
      echo "Invalid option: -OPTARG" >&2
      exit 1
      ;;
  esac
done

if [[ "$delete" == "true" ]]; then
  find . -name "node_modules" -type d -prune -print
else
  find . -name "node_modules" -type d -prune -print
fi
